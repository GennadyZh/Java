package day6;

class Car {
    private int age;
    private String color;
    private String model;

    public void setAge (int ageOut){
        age = ageOut;
    }

    public int getAge(){
        return age;
    }

    public void setColor (String colorAuto){
        color = colorAuto;
    }

    public String getColor(){
        return color;
    }

    public void setModel (String modelAuto){
        model = modelAuto;
    }

    public String getModel(){
        return model;
    }

}
